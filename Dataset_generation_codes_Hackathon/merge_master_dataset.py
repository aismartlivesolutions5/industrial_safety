# merge_master_dataset.py
# Merge IsolationForest anomalies + XGBoost risk predictions into one MASTER dataset
# Output:
#  1) bulk_drug_factory_MASTER_scored.csv
#  2) daily_asset_summary.csv (for dashboard analytics)
#  3) high_risk_events.csv (for audit report generation)

import os
import json
import numpy as np
import pandas as pd
import xgboost as xgb

# =========================
# PATHS
# =========================
BASE_DIR  = "/teamspace/studios/this_studio/Dataset_generation_codes/Hackathon"
DATA_DIR  = os.path.join(BASE_DIR, "data")
MODEL_DIR = os.path.join(BASE_DIR, "models")

# Input (already generated by you)
IF_SCORED_CSV = os.path.join(DATA_DIR, "bulk_drug_factory_with_if_anomalies.csv")

# XGB model files (already generated by you)
XGB_MODEL_JSON = os.path.join(MODEL_DIR, "bulk_drug_safety_xgb_cpu_es_model.json")
XGB_META_JSON  = os.path.join(MODEL_DIR, "bulk_drug_safety_xgb_cpu_es_model_metadata.json")

# Optional (already generated by you)
SHAP_GLOBAL_JSON = os.path.join(MODEL_DIR, "bulk_drug_xgb_shap_global.json")

# Outputs
OUT_MASTER_CSV   = os.path.join(DATA_DIR, "bulk_drug_factory_MASTER_scored.csv")
OUT_DAILY_SUMM   = os.path.join(DATA_DIR, "daily_asset_summary.csv")
OUT_EVENTS_CSV   = os.path.join(DATA_DIR, "high_risk_events.csv")


def load_xgb_booster():
    if not os.path.exists(XGB_MODEL_JSON):
        raise FileNotFoundError(f"Missing XGB model json: {XGB_MODEL_JSON}")
    booster = xgb.Booster()
    booster.load_model(XGB_MODEL_JSON)
    return booster


def load_xgb_meta():
    if not os.path.exists(XGB_META_JSON):
        raise FileNotFoundError(f"Missing XGB metadata json: {XGB_META_JSON}")
    with open(XGB_META_JSON, "r") as f:
        meta = json.load(f)
    features = meta["features"]
    class_map = meta["class_mapping"]  # keys are strings: "0","1","2","3"
    return meta, features, class_map


def load_shap_global():
    """Optional: load global mean abs shap for dashboard display."""
    if not os.path.exists(SHAP_GLOBAL_JSON):
        return None
    with open(SHAP_GLOBAL_JSON, "r") as f:
        return json.load(f)


def xgb_predict(df: pd.DataFrame, booster: xgb.Booster, features: list, class_map: dict):
    # Ensure all features exist
    missing = [c for c in features if c not in df.columns]
    if missing:
        raise ValueError(f"Missing XGB feature columns in IF dataset: {missing}")

    X = df[features].astype(np.float32)
    dmat = xgb.DMatrix(X, feature_names=features)

    proba = booster.predict(dmat)  # (N, num_classes)
    if proba.ndim != 2:
        raise RuntimeError(f"Unexpected predict shape: {proba.shape}")

    pred_class = np.argmax(proba, axis=1).astype(int)
    pred_prob  = np.max(proba, axis=1).astype(np.float32)

    # map class to label
    pred_label = [class_map.get(str(i), str(i)) for i in pred_class]

    # add probability columns (optional but useful for dashboard)
    out = pd.DataFrame({
        "risk_pred_class": pred_class,
        "risk_label": pred_label,
        "risk_confidence": pred_prob
    })
    for k in range(proba.shape[1]):
        out[f"risk_p{k}"] = proba[:, k].astype(np.float32)

    return out


def build_daily_summary(master: pd.DataFrame) -> pd.DataFrame:
    # daily rollups for dashboard analytics
    master["date"] = pd.to_datetime(master["timestamp"]).dt.date

    # Convert levels into numeric severity
    alert_sev_map = {"Normal": 0, "Watch": 1, "High": 2, "Critical": 3}
    risk_sev_map  = {"Normal": 0, "Watch": 1, "High Risk": 2, "Critical": 3}

    master["alert_sev"] = master["alert_level"].map(alert_sev_map).fillna(0).astype(int)
    master["risk_sev"]  = master["risk_label"].map(risk_sev_map).fillna(0).astype(int)

    daily = master.groupby(["date", "asset_id"], as_index=False).agg(
        rows=("timestamp", "count"),
        max_anomaly=("anomaly_score", "max"),
        mean_anomaly=("anomaly_score", "mean"),
        max_alert_sev=("alert_sev", "max"),
        max_risk_sev=("risk_sev", "max"),
        critical_alert_minutes=("alert_level", lambda x: int((x == "Critical").sum())),
        high_alert_minutes=("alert_level", lambda x: int((x == "High").sum())),
        critical_risk_minutes=("risk_label", lambda x: int((x == "Critical").sum())),
        highrisk_minutes=("risk_label", lambda x: int((x == "High Risk").sum())),
    )

    # Convert sev back to labels for readability
    inv_alert = {v: k for k, v in alert_sev_map.items()}
    inv_risk  = {v: k for k, v in risk_sev_map.items()}
    daily["daily_max_alert_level"] = daily["max_alert_sev"].map(inv_alert)
    daily["daily_max_risk_level"]  = daily["max_risk_sev"].map(inv_risk)

    return daily


def build_high_risk_events(master: pd.DataFrame) -> pd.DataFrame:
    # events for audit report: anything High/Critical either from IF or XGB
    mask = (
        master["alert_level"].isin(["High", "Critical"])
        | master["risk_label"].isin(["High Risk", "Critical"])
    )
    events = master.loc[mask, [
        "timestamp", "asset_id",
        "boiler_pressure_bar", "boiler_temperature_c",
        "voc_ppm", "nh3_ppm", "h2s_ppm", "lel_percent",
        "vibration_rms", "active_alarm_count", "days_since_last_maintenance",
        "anomaly_score", "alert_level",
        "risk_label", "risk_confidence"
    ]].copy()

    events.sort_values(["timestamp", "asset_id"], inplace=True)
    return events


def main():
    if not os.path.exists(IF_SCORED_CSV):
        raise FileNotFoundError(f"Missing IF scored dataset: {IF_SCORED_CSV}")

    df = pd.read_csv(IF_SCORED_CSV)
    df["timestamp"] = pd.to_datetime(df["timestamp"])

    # Load XGB
    booster = load_xgb_booster()
    meta, features, class_map = load_xgb_meta()

    # Predict risk using XGB
    risk_df = xgb_predict(df, booster, features, class_map)

    # Merge into master
    master = pd.concat([df.reset_index(drop=True), risk_df.reset_index(drop=True)], axis=1)

    # Optional: attach global shap summary (for dashboard display)
    shap_global = load_shap_global()
    if shap_global and "global_mean_abs_shap" in shap_global:
        # store top 5 features as a string column (same value repeated; dashboard can read once)
        top5 = sorted(shap_global["global_mean_abs_shap"].items(), key=lambda x: -x[1])[:5]
        master["xgb_global_top5"] = str(top5)

    # Save master
    master.to_csv(OUT_MASTER_CSV, index=False)

    # Save daily summary
    daily = build_daily_summary(master)
    daily.to_csv(OUT_DAILY_SUMM, index=False)

    # Save risk events for audit report
    events = build_high_risk_events(master)
    events.to_csv(OUT_EVENTS_CSV, index=False)

    print("\n=== MASTER MERGE DONE ===")
    print("MASTER CSV :", OUT_MASTER_CSV)
    print("DAILY SUMM :", OUT_DAILY_SUMM)
    print("EVENTS CSV :", OUT_EVENTS_CSV)
    print("\nSanity checks:")
    print("Rows:", master.shape[0], "Cols:", master.shape[1])
    print("Alert distribution:", master["alert_level"].value_counts(normalize=True).round(4).to_dict())
    print("Risk  distribution:", master["risk_label"].value_counts(normalize=True).round(4).to_dict())


if __name__ == "__main__":
    main()
